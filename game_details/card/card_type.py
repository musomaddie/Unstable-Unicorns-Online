from enum import Enum


class CardType(Enum):
    """ Represents the possible types of cards. """
    BABY_UNICORN = "baby unicorn"
    BASIC_UNICORN = "basic unicorn"
    DOWNGRADE = "downgrade"
    INSTANT = "instant"
    MAGIC = "magic"
    MAGIC_UNICORN = "magic unicorn"
    UPGRADE = "upgrade"
